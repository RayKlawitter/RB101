a = [5, 9, 3, 11]
print a.min(2)

#  PROBLEM
#  We want to use the .min method on an array to print the two smallest values in that array
#  .min can accept parameters and since we want TWO smallest values in the array we can do .min(2)
#
#  Examples
#  From the documentation we can use .min(*)
#  [0, 1, 2, 3].min => 0
#  [0, 1, 2, 3].min(2) => [0, 1]
#  [0, 1, 2, 3].min(5) => [0, 1, 2, 3]
#
#  Data Structure
#  An array of integers
#
#  Algorithm
#  a = [5, 9, 3, 11]
#  a.min(2) => [3, 5]
